# -*- coding: utf-8 -*-
"""
text_features.py — интерпретируемые фичи из текста объявления для модели цены.

Почему НЕ эмбеддинги/TF-IDF на этом этапе: медиана текста 51 символ и лишь
27% строк имеют полный комментарий (остальное — огрызок листинга). На таком
эмбеддинги шумны и непрозрачны. Берём прозрачные сигналы, прямо двигающие цену:
  • опции/комплектация (кожа, панорама, камера, макс.компл) → дороже;
  • заявления о состоянии (не бит, один хозяин, на гарантии)  → дороже;
  • damage-слова (единый лексикон damage.py)                 → дешевле;
  • срочность                                                → слабый сигнал;
  • длина текста                                             → прокси детальности.

Интерпретируемо (feature importance объясним словами), дёшево, без утечки
(всё доступно в момент публикации). Чистые функции — тестируются без сети.

СТАТУС (измерено 2026-07-23): в модель цены пока НЕ включено. На текущих
данных (медиана текста 51 символ, лишь 27% полных комментов) фичи дали flat:
R²=0.914 без изменений, MAPE 24.0→24.5, суммарная важность ~3% — сигнал
избыточен с brand/engine_volume/body_type. Возвращаем в train_price_model
после backfill (больше полных комментов → TF-IDF/эмбеддинги). Модуль готов,
покрыт тестом; также полезен для фрод-сигналов (txt_damage/txt_urgency).
"""

import re

from damage import has_damage as _has_damage

# Группы премиум-опций (каждая совпавшая группа = +1 к opt_count).
_OPTION_GROUPS = [
    r"кож[аеи]", r"панорам|люк", r"камер", r"подогрев", r"webasto|вебасто",
    r"мультимед|андроид|carplay", r"макс\w*\s+компл|полн\w*\s+компл",
    r"турбо", r"рестайлинг", r"вентиляц",
]
# Позитивные заявления о состоянии/истории.
_POSITIVE = re.compile(
    r"не\s*бит|небит|не\s*краш|некраш|родн\w+\s+краск|один хозяин|1 хозяин|"
    r"один владел|идеальн\w*\s+сост|на гарантии|обслуж")
_URGENCY = re.compile(r"срочн")

TEXT_FEATURE_COLS = ["txt_len", "txt_opt_count", "txt_damage",
                     "txt_positive", "txt_urgency"]


def text_features(text) -> dict:
    """text → {фичи}. Чистая функция."""
    t = str(text or "").lower()
    opt = sum(1 for g in _OPTION_GROUPS if re.search(g, t))
    return {
        "txt_len":       len(t),
        "txt_opt_count": opt,
        "txt_damage":    int(_has_damage(t)),
        "txt_positive":  int(bool(_POSITIVE.search(t))),
        "txt_urgency":   int(bool(_URGENCY.search(t))),
    }
