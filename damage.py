# -*- coding: utf-8 -*-
"""
damage.py — лексикон «убитости» + УМНЫЙ поиск с учётом отрицаний.

Раньше лексикон был продублирован в clean.py и enrich.py (с
тестом-сторожем на синхронность), а поиск был наивной подстрокой.
Наивность стоила точности в ОПАСНУЮ сторону: «На 99% нету никаких
гнилей», «Вложения не требует», «не требует вложений» — всё это
ПОЗИТИВНЫЕ утверждения о состоянии, но подстрока матчила их как
повреждения, и объявление получало ложное оправдание дешевизны
(exculpation). Мошеннику достаточно написать «не битая, не гнилая»,
чтобы наивный детектор сам снял с него флаг.

Решение — не полноценный NLP (морфология/синтаксис здесь из пушки по
воробьям), а проверка ОКНА ОТРИЦАНИЙ вокруг совпадения:
  1) до совпадения (≤2 токенов): «не», «нет», «нету», «без»,
     «никаких»... — «нету никаких гнилей», «не требует вложений»;
  2) сразу после совпадения: «<слово> не требует / нет / отсутствует» —
     «вложения не требует», «ржавчины нет».

Известное ограничение (осознанное): «не гнилая, но битая» потеряет
оба хита (отрицание перед «гнилая» попадает в окно «битая»). Такие
конструкции редки; цена ложного оправдания выше цены редкого пропуска.
"""

import re

DAMAGE_PATTERNS = [
    # прямые слова о повреждениях
    "ржавчин", "гнил", "сгнил", "на запчасти", "по запчастям", "на разбор",
    "не на ходу", "после дтп", "после аварии", "аварийн", "битая", "битый",
    "утопленник",
    # ремонт и вложения
    "требует ремонта", "нужен ремонт", "требует вложений", "нужны вложения",
    "вложения", "под восстановление", "не заводится", "не работает",
    "двигатель стучит", "мотор стучит",
    # хитрые формулировки: прошедшее время = сейчас НЕ на ходу
    "был на ходу", "была на ходу", "был находу", "была находу",
    # отсутствие агрегатов/документов. Продавцы пишут как попало —
    # держим и разговорные варианты, и частые ошибки написания
    # («без матора» — реальный кейс из базы). Склонения: подстрочный
    # поиск не знает морфологии — «вложения» не матчит «вложений»,
    # поэтому фразовые варианты. Голый корень «вложени» нельзя:
    # «БЕЗ вложений» — позитив (его теперь режет и окно отрицаний).
    "есть вложения", "с вложениями", "кузову вложений", "вложений по",
    "без двигателя", "нет двигателя", "без мотора", "без матора",
    "нет мотора", "нет матора", "без коробки", "нет коробки",
    "без кпп", "без акпп", "без документов", "нет документов",
    "снята с учета", "снята с учёта",
]

# Отрицания ПЕРЕД совпадением. Окно = 2 токена: шире нельзя —
# постороннее отрицание за 3 слова до хита («салон НЕ прокурен,
# есть ржавчина») убивало честное повреждение. Все реальные кейсы
# отрицаний укладываются в 2: "нету никаких гнилей",
# "не требует вложений", "без единой ржавчины"
_NEG_BEFORE = {"не", "нет", "нету", "без", "ни", "никаких", "никакой",
               "никаким", "отсутствует", "отсутствуют"}

# Отрицания СРАЗУ ПОСЛЕ совпадения: "вложения не требует", "гнили нет".
# Узкий список глаголов намеренно: общее «не» после хита резало бы
# честное «битая, не крашена».
_NEG_AFTER = re.compile(
    r"^\s*(не\s+(требует|требуется|нужны|нужно|надо|было|имеет)"
    r"|нет|нету|отсутству\w*)\b")

_TOKEN = re.compile(r"[а-яё\d%]+")


def find_damage_keywords(text) -> list[str]:
    """Совпадения лексикона в тексте, ЗА ВЫЧЕТОМ отрицаемых.
    Чистая функция (текст → список), тестируется без сети и файлов."""
    t = str(text or "").lower()
    hits = []
    for p in DAMAGE_PATTERNS:
        # Паттерны, САМИ начинающиеся с отрицания («без коробки»,
        # «нет мотора», «не на ходу»), окном «отрицание перед» не
        # проверяем: отрицание — их собственная суть, а соседняя
        # фраза («без матора, БЕЗ коробки») ложно гасила бы хит.
        skip_before = p.split()[0] in _NEG_BEFORE
        for m in re.finditer(re.escape(p), t):
            before = _TOKEN.findall(t[:m.start()])[-2:]
            if not skip_before and any(w in _NEG_BEFORE for w in before):
                continue
            if _NEG_AFTER.match(t[m.end():]):
                continue
            hits.append(p)
            break   # одного подтверждённого вхождения достаточно
    return sorted(set(hits))


def has_damage(text) -> bool:
    return bool(find_damage_keywords(text))
